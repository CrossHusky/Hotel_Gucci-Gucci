    public void ConfiguracionLabels(){  // Acomoda toda la informacion optenida en el objeto de la clase Huesped. para mostrarla con JLabels.
        this.jLabelNombreH.setFont(fuente(0, 11));
        this.jLabelCiudad.setFont(fuente(0, 11));
        this.jLabelFechaIng.setFont(fuente(0, 11));
        this.jLabelFechaSal.setFont(fuente(0, 11));
        this.jLabelNumeroHYPiso.setFont(fuente(0, 11));
        this.jLabelTipoYLimite.setFont(fuente(0, 11));
        this.jLabelTotalPersonas.setFont(fuente(0,11));
        this.jLabelExtras.setFont(fuente(0, 11));
        this.jLabelRegistroCompleto.setFont(fuente(0, 11));
        this.jLabelUbicacion1.setFont(fuente(0, 11));
        this.jLabelUbicacion2.setFont(fuente(0, 11));
        this.jLabelUbicacion3.setFont(fuente(0, 11));
        
        SimpleDateFormat Formato = new SimpleDateFormat("dd-MM-YYYY");
        
         this.jLabelNombreH.setText("NOMBRE DEL HUESPED: ------------ "+huesped.getNombre());
          this.jLabelCiudad.setText("CIUDAD DE ORIGEN: --------------- "+huesped.getCiudadOrigen());
        this.jLabelFechaIng.setText("FECHA DE INGRESO: --------------- "+Formato.format(huesped.getFechaIngreso()));
        
        Calendar aux = Calendar.getInstance();
        aux.setTime(huesped.getFechaIngreso());
        aux.add(Calendar.DATE, huesped.getDiasAlojo());
        this.jLabelFechaSal.setText("FECHA DE SALIDA: ---------------- "+Formato.format(aux.getTime()));
        int piso;
        if(huesped.getNumeroHabitacion()<=15) piso=1;else piso=2;
        this.jLabelNumeroHYPiso.setText("NUMERO DE HABITACION:  "+huesped.getNumeroHabitacion()+"   PISO: "+piso);
        switch(huesped.getTipoHabitacion()){
            case 'S':{
                this.jLabelTipoYLimite.setText("TIPO DE HABITACION:  Simple   (LIMITE 3 HUESPEDES)");
                if (huesped.getTotalPersonas()>1) {
                    if(huesped.getTotalPersonas()==2){
                        this.jLabelExtras.setText("1 Persona Extra.");
                    }
                    else{
                        this.jLabelExtras.setText("2 Personas Extra.");
                    }
                }
                else{
                    this.jLabelExtras.setVisible(false);
                }
                break;
            }
            case 'D':{
                this.jLabelTipoYLimite.setText("TIPO DE HABITACION:     Doble       (LIMITE 4 HUESPEDES)");
                if (huesped.getTotalPersonas()>2) {
                    if(huesped.getTotalPersonas()==3){
                        this.jLabelExtras.setText("1 Persona Extra.");
                    }
                    else{
                        this.jLabelExtras.setText("2 Personas Extra.");
                    }
                }
                else{
                    this.jLabelExtras.setVisible(false);
                }
                break;
            }
            case 'T':{
                this.jLabelTipoYLimite.setText("TIPO DE HABITACION:     Triple      (LIMITE 5 HUESPEDES)");
                if (huesped.getTotalPersonas()>3) {
                    if(huesped.getTotalPersonas()==4){
                        this.jLabelExtras.setText("1 Persona Extra.");
                    }
                    else{
                        this.jLabelExtras.setText("2 Personas Extra.");
                    }
                }
                else{
                    this.jLabelExtras.setVisible(false);
                }
                break;
            }
        }
        this.jLabelTotalPersonas.setText("TOTAL DE OCUPANTES EN LA HABITACION:      "+huesped.getTotalPersonas());
    }

