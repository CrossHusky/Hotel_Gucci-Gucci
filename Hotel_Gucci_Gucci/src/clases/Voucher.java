    private Font fuente(int estilo, float tamanio){ // Cargar la Funte para usarla en el Voucher.
         try {
            InputStream is =  getClass().getResourceAsStream("/librerias/FunteTiquet.ttc");
            font = Font.createFont(Font.TRUETYPE_FONT, is);
        } catch (Exception ex) {
            //Si existe un error se carga fuente por defecto ARIAL
            System.err.println("FunteTiquet.ttc" + " No se cargo la fuente");
            font = new Font("Arial", Font.PLAIN, 20);            
        }
        Font tfont = font.deriveFont(estilo, tamanio);
        return tfont;
    }
